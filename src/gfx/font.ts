/** Draws the full font strip (white glyphs) at (ox,oy); width = chars*GLYPH_W. */
export function drawFontStrip(ctx: CanvasRenderingContext2D, ox: number, oy: number): void {
  ctx.fillStyle = '#ffffff';
  for (let i = 0; i < FONT_CHARS.length; i++) {
    const ch = FONT_CHARS[i]!;
    const glyph = G[ch];
    if (!glyph) continue;
    for (let y = 0; y < 5; y++) {
      const row = glyph[y]!;
      for (let x = 0; x < 3; x++) {
        if (row[x] === '#') ctx.fillRect(ox + i * GLYPH_W + x, oy + y, 1, 1);
      }
    }
  }
}

export function fontStripWidth(): number {
  return FONT_CHARS.length * GLYPH_W;
}
